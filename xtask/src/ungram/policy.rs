use alloc::collections::BTreeSet;
use core::error::Error;
use std::fs;

use heck::ToShoutySnakeCase as _;
use quote::{format_ident, quote};
use ungrammar::Grammar;

const GRAMMAR: &str = "grammar/policies.ungram";
const OUTPUT: &str = "src/policy/syntax.rs";

const PUNCTUATION: &[(&str, &str)] = &[
    ("@", "AT"),
    ("(", "L_PAREN"),
    (")", "R_PAREN"),
    ("{", "L_BRACE"),
    ("}", "R_BRACE"),
    ("[", "L_BRACKET"),
    ("]", "R_BRACKET"),
    (";", "SEMI"),
    (":", "COLON"),
    ("::", "COLON2"),
    (",", "COMMA"),
    (".", "DOT"),
    ("?", "QUESTION"),
    ("==", "EQ"),
    ("!=", "NEQ"),
    ("<", "LT"),
    ("<=", "LTEQ"),
    (">", "GT"),
    (">=", "GTEQ"),
    ("&&", "AMP2"),
    ("||", "PIPE2"),
    ("+", "PLUS"),
    ("-", "MINUS"),
    ("*", "STAR"),
    ("/", "SLASH"),
    ("%", "PERCENT"),
    ("!", "BANG"),
    ("?principal", "PRINCIPAL_SLOT"),
    ("?resource", "RESOURCE_SLOT"),
];

const TERMINALS: &[(&str, &str)] = &[
    ("anyident", "IDENT"),
    ("ident", "IDENT"),
    ("str", "STRING"),
    ("pat", "PATTERN"),
    ("int", "INT"),
];

const TRIVIAL: &[&str] = &["WHITESPACE", "COMMENT"];
const SPECIAL: &[&str] = &["ERROR", "EOF"];
const LITERALS: &[&str] = &["INT", "STRING", "PATTERN"];

pub fn generate() -> Result<(), Box<dyn Error>> {
    let source = fs::read_to_string(GRAMMAR)?;
    let grammar: Grammar = source.parse()?;

    let mut kinds = BTreeSet::new();
    let mut keywords = BTreeSet::new();

    for (_, kind) in PUNCTUATION {
        kinds.insert(kind.to_string());
    }

    for (_, kind) in TERMINALS {
        kinds.insert(kind.to_string());
    }

    for kind in TRIVIAL {
        kinds.insert(kind.to_string());
    }

    for kind in SPECIAL {
        kinds.insert(kind.to_string());
    }

    for kind in LITERALS {
        kinds.insert(kind.to_string());
    }

    for token in grammar.tokens() {
        let name = &grammar[token].name;

        // Terminal
        if let Some((_, kind)) = TERMINALS.iter().find(|(token, _)| *token == name) {
            kinds.insert((*kind).to_owned());
            continue;
        }

        // Keywords
        if name.chars().all(|char| char.is_alphabetic() || char == '_') {
            let kind = format!("{}_KW", name.to_shouty_snake_case());
            keywords.insert(kind.clone());
            kinds.insert(kind);
        }
    }

    for node in grammar.iter() {
        let name = &grammar[node].name;
        let kind = name.to_shouty_snake_case();
        kinds.insert(kind);
    }

    let variants: Vec<_> = kinds.iter().map(|kind| format_ident!("{kind}")).collect();

    let trivial: Vec<_> = TRIVIAL.iter().map(|kind| format_ident!("{kind}")).collect();
    let punctuation: Vec<_> = PUNCTUATION
        .iter()
        .map(|(_, kind)| format_ident!("{kind}"))
        .collect();
    let keywords: Vec<_> = keywords
        .iter()
        .map(|kind| format_ident!("{kind}"))
        .collect();
    let literals: Vec<_> = LITERALS
        .iter()
        .map(|kind| format_ident!("{kind}"))
        .collect();

    let tokens = quote! {
        //! Generated by `cargo xtask ungram policy`.

        /// Syntax kinds for Cedar policies.
        #[derive(Debug, Clone, Copy, PartialEq, Eq, PartialOrd, Ord, Hash)]
        #[repr(u8)]
        #[expect(
            non_camel_case_types,
            clippy::upper_case_acronyms,
            reason = "Generated code"
        )]
        pub enum PolicyKind {
            #(#variants,)*
        }

        impl PolicyKind {
            /// Returns `true` if this is trivial.
            #[inline]
            #[must_use]
            pub const fn is_trivial(self) -> bool {
                matches!(self, #(Self::#trivial)|*)
            }

            /// Returns `true` if this is a keyword.
            #[inline]
            #[must_use]
            pub const fn is_keyword(self) -> bool {
                matches!(self, #(Self::#keywords)|*)
            }

            /// Returns `true` if this is punctuation.
            #[inline]
            #[must_use]
            pub const fn is_punctuation(self) -> bool {
                matches!(self, #(Self::#punctuation)|*)
            }

            /// Returns `true` if this is a literal.
            #[inline]
            #[must_use]
            pub const fn is_literal(self) -> bool {
                matches!(self, #(Self::#literals)|*)
            }

            /// Returns `true` if this is an error.
            #[inline]
            #[must_use]
            pub const fn is_error(self) -> bool {
                matches!(self, Self::ERROR)
            }
        }
    };

    let syntax = syn::parse_file(&tokens.to_string())?;
    let output = prettyplease::unparse(&syntax);
    fs::write(OUTPUT, output)?;

    Ok(())
}
